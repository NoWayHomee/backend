-- 2. Bảng BOOKINGS
CREATE TABLE bookings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  booking_code VARCHAR(30) NOT NULL,
  customer_id BIGINT NOT NULL,
  property_id BIGINT NOT NULL,
  check_in_date DATE NOT NULL,
  check_out_date DATE NOT NULL,
  num_nights SMALLINT NOT NULL,
  num_adults SMALLINT NOT NULL DEFAULT 1,
  num_children SMALLINT NOT NULL DEFAULT 0,
  subtotal_amount DECIMAL(12, 2) NOT NULL,
  discount_amount DECIMAL(12, 2) NOT NULL DEFAULT 0.00,
  tax_amount DECIMAL(12, 2) NOT NULL DEFAULT 0.00,
  total_amount DECIMAL(12, 2) NOT NULL,
  platform_fee_amount DECIMAL(12, 2) NOT NULL DEFAULT 0.00,
  partner_payout_amount DECIMAL(12, 2) NOT NULL DEFAULT 0.00,
  currency CHAR(3) NOT NULL DEFAULT 'VND',
  voucher_id BIGINT DEFAULT NULL,
  loyalty_points_used INT NOT NULL DEFAULT 0,
  status booking_status_enum NOT NULL DEFAULT 'pending',
  payment_status payment_status_enum NOT NULL DEFAULT 'unpaid',
  source_channel source_channel_enum NOT NULL DEFAULT 'web',
  special_requests TEXT DEFAULT NULL,
  cancellation_reason TEXT DEFAULT NULL,
  cancelled_by BIGINT DEFAULT NULL,
  cancelled_at TIMESTAMP DEFAULT NULL,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT uq_booking_code UNIQUE (booking_code),
  CONSTRAINT fk_booking_cust FOREIGN KEY (customer_id) REFERENCES users (id),
  CONSTRAINT fk_booking_prop FOREIGN KEY (property_id) REFERENCES properties (id),
  CONSTRAINT fk_booking_voucher FOREIGN KEY (voucher_id) REFERENCES vouchers (id) ON DELETE
  SET NULL,
    CONSTRAINT fk_booking_canceler FOREIGN KEY (cancelled_by) REFERENCES users (id) ON DELETE
  SET NULL,
    CONSTRAINT chk_booking_dates CHECK (check_in_date < check_out_date),
    -- Postgres trừ 2 date ra integer:
    CONSTRAINT chk_booking_nights CHECK (num_nights = (check_out_date - check_in_date)),
    CONSTRAINT chk_booking_total CHECK (total_amount >= 0)
);
CREATE INDEX idx_booking_customer ON bookings (customer_id);
CREATE INDEX idx_booking_property ON bookings (property_id);
