-- 2. Bảng PROPERTIES
CREATE TABLE properties (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  partner_id BIGINT NOT NULL,
  slug VARCHAR(300) NOT NULL,
  name VARCHAR(500) NOT NULL,
  property_type property_type_enum NOT NULL,
  description TEXT DEFAULT NULL,
  address TEXT NOT NULL,
  city VARCHAR(100) NOT NULL,
  district VARCHAR(100) DEFAULT NULL,
  country_code CHAR(2) NOT NULL DEFAULT 'VN',
  latitude DECIMAL(10, 8) NOT NULL,
  longitude DECIMAL(11, 8) NOT NULL,
  star_rating SMALLINT DEFAULT NULL,
  -- Postgres dùng SMALLINT thay cho TINYINT
  avg_rating DECIMAL(3, 2) NOT NULL DEFAULT 0.00,
  total_reviews INT NOT NULL DEFAULT 0,
  check_in_time TIME NOT NULL DEFAULT '14:00:00',
  check_out_time TIME NOT NULL DEFAULT '12:00:00',
  status property_status_enum NOT NULL DEFAULT 'draft',
  reviewed_by BIGINT DEFAULT NULL,
  reviewed_at TIMESTAMP DEFAULT NULL,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT uq_property_slug UNIQUE (slug),
  CONSTRAINT fk_property_partner FOREIGN KEY (partner_id) REFERENCES partner_profiles (id),
  CONSTRAINT fk_property_reviewer FOREIGN KEY (reviewed_by) REFERENCES users (id) ON DELETE
  SET NULL,
    CONSTRAINT chk_star_rating CHECK (
      star_rating BETWEEN 1 AND 5
    )
);
-- Index cho Properties
